        # If both empty
        if not p and not q:
            return True

        # If only one empty
        if p and not q:
            return False

        if not p and q:
            return False

        # If the vals are diffrent
        if p.val != q.val:
            return False
        
        # Check recursively
        else:
            return self.isSameTree(p.left, q.left) and self.isSameTree(p.right, q.right)

        












































